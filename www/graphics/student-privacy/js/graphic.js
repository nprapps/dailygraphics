function showItems($el){
    var items = $el.find('li');
    var delay = 0;

    $el.fadeIn();

    items.each(function(i) {
        delay = (i + 1) * 50;
        $(this).delay(delay).show({
            // duration: 150,
            // easing: 'easeInOutCubic',
            progress: function(){
                pymChild.sendHeightToParent();
            },
            done: function(){
                $(this).find('span').css('opacity',1);
            }
        });
    });
}

function onShowNextClick(){
    var $this = $(this);
    var $nextGrade = $this.parents('.grade').next();

    $this.addClass('done').text('+');
    showItems($nextGrade);
}


function onStartClick(){
    var $this = $(this);
    var $firstSection = $('.grade').first();
    showItems($firstSection);
    $this.fadeOut();
}

$(function(){
    $next = $('.show-next');
    $start = $('.start');

    $next.on('click', onShowNextClick);
    $start.on('click', onStartClick);
});

